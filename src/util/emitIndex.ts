import { readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { resolve } from 'node:path';
import type { ManifestEntry } from '../api.js';
import { assertSafeAssetSegments } from './security.js';

/**
 * Emit `<outDir>/index.ts` with a typed map of every asset key → URL.
 *
 *   import { MagicPixelAssets } from '@/assets/magicpixel';
 *   const url = MagicPixelAssets['player/walk'];
 *
 * Vite, webpack, esbuild, Parcel, Rollup, etc. all hash these URLs at build
 * time. No runtime, no bundler config.
 */
export async function emitTypedIndex(
  outDir: string,
  manifest: ManifestEntry[],
  cwd: string = process.cwd(),
): Promise<string> {
  // Dedupe by key. A well-behaved manifest never has duplicates, but a server
  // bug here would otherwise emit duplicate object keys and break TS compile.
  const seen = new Set<string>();
  const sorted = [...manifest]
    .sort((a, b) => a.key.localeCompare(b.key))
    .filter((e) => {
      if (seen.has(e.key)) {
        console.warn(`magicpixel: duplicate manifest key "${e.key}" — keeping first.`);
        return false;
      }
      seen.add(e.key);
      return true;
    });
  const lines: string[] = [
    '// AUTOGENERATED by @magicpixelart/cli. Do not edit.',
    '// Re-runs of `magicpixel sync` overwrite this file.',
    '',
  ];
  // Use positional identifiers (asset_0, asset_1, …) so keys that differ only
  // by non-alphanumeric chars (e.g. `player/walk` vs `player_walk`) can't
  // collide on the JS identifier and break the consumer's TS build.
  sorted.forEach((e, i) => {
    assertSafeAssetSegments(e.folder, e.slug, e.key);
    const rel = e.folder ? `./${e.folder}/${e.slug}.png` : `./${e.slug}.png`;
    lines.push(`import asset_${i} from ${JSON.stringify(rel)};`);
  });
  lines.push('');
  lines.push('export const MagicPixelAssets = {');
  sorted.forEach((e, i) => {
    lines.push(`  ${JSON.stringify(e.key)}: asset_${i},`);
  });
  lines.push('} as const;');
  lines.push('');
  lines.push('export type MagicPixelAssetKey = keyof typeof MagicPixelAssets;');
  lines.push('');

  const path = resolve(cwd, outDir, 'index.ts');
  const next = lines.join('\n');

  // Skip the write when contents are identical. Avoids triggering Vite/webpack
  // HMR reloads on every `--watch` poll when nothing actually changed.
  if (existsSync(path)) {
    try {
      const current = await readFile(path, 'utf8');
      if (current === next) return path;
    } catch {
      // fall through and overwrite
    }
  }
  await writeFile(path, next, 'utf8');
  return path;
}
